import streamlit as st
import google.generativeai as genai
import numpy as np
import faiss
import os
from PyPDF2 import PdfReader

# ---------------------------
# CONFIGURE GEMINI
# ---------------------------

genai.configure(api_key=st.secrets["GEMINI_API_KEY"])
model = genai.GenerativeModel("gemini-1.5-flash")

CHUNK_SIZE = 800
CHUNK_OVERLAP = 100


# ---------------------------
# CHUNKING
# ---------------------------
def chunk_text(text):
    chunks = []
    for i in range(0, len(text), CHUNK_SIZE - CHUNK_OVERLAP):
        chunks.append(text[i:i + CHUNK_SIZE])
    return chunks


# ---------------------------
# EMBEDDING USING GEMINI
# ---------------------------
def get_embedding(text):
    response = genai.embed_content(
        model="models/embedding-001",
        content=text
    )
    return response["embedding"]


# ---------------------------
# CREATE VECTOR STORE
# ---------------------------
def create_vectorstore(text):
    chunks = chunk_text(text)

    embeddings = []
    for chunk in chunks:
        embeddings.append(get_embedding(chunk))

    embeddings = np.array(embeddings).astype("float32")

    dimension = embeddings.shape[1]
    index = faiss.IndexFlatL2(dimension)
    index.add(embeddings)

    return index, chunks


# ---------------------------
# RETRIEVE
# ---------------------------
def retrieve(query, index, chunks, top_k=3):
    query_embedding = np.array([get_embedding(query)]).astype("float32")
    distances, indices = index.search(query_embedding, top_k)
    return [chunks[i] for i in indices[0]]


# ---------------------------
# STREAMLIT UI
# ---------------------------

st.title("ðŸ”® Gemini Multimodal RAG Chat")

if "chat_history" not in st.session_state:
    st.session_state.chat_history = []

uploaded_file = st.file_uploader("Upload PDF or Image", type=["pdf", "png", "jpg", "jpeg"])
query = st.chat_input("Ask your question")

text_data = ""

# ---------------------------
# FILE PROCESSING
# ---------------------------

if uploaded_file:

    if uploaded_file.type == "application/pdf":
        reader = PdfReader(uploaded_file)
        for page in reader.pages:
            text_data += page.extract_text()

    else:
        # Gemini can directly read images
        image_bytes = uploaded_file.read()
        response = model.generate_content([
            "Extract all readable text from this image.",
            {"mime_type": uploaded_file.type, "data": image_bytes}
        ])
        text_data = response.text

    st.success("File processed.")


# ---------------------------
# MAIN CHAT
# ---------------------------

if query and text_data:

    index, chunks = create_vectorstore(text_data)
    context = retrieve(query, index, chunks)

    prompt = f"""
    Answer strictly using the context below.

    Context:
    {context}

    Question:
    {query}

    If not found, say:
    Answer not found in provided document.
    """

    response = model.generate_content(prompt)

    st.session_state.chat_history.append(("user", query))
    st.session_state.chat_history.append(("assistant", response.text))


# ---------------------------
# DISPLAY CHAT
# ---------------------------

for role, message in st.session_state.chat_history:
    with st.chat_message(role):
        st.write(message)

