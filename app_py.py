import streamlit as st
import requests
from PIL import Image
import io

# ---------------- CONFIG ---------------- #

HF_API_KEY = st.secrets["HF_API_KEY"]

MODEL_URL = "https://api-inference.huggingface.co/models/Salesforce/blip-image-captioning-large"

headers = {
    "Authorization": f"Bearer {HF_API_KEY}"
}

# ---------------- HUGGING FACE CALL ---------------- #

def query_image(image_bytes):
    response = requests.post(
        MODEL_URL,
        headers=headers,
        data=image_bytes
    )
    return response.json()

# ---------------- STREAMLIT UI ---------------- #

st.title("ðŸ–¼ Hugging Face Vision Assistant")

uploaded_file = st.file_uploader(
    "Upload an Image",
    type=["png", "jpg", "jpeg"]
)

if uploaded_file:
    image = Image.open(uploaded_file)
    st.image(image, caption="Uploaded Image", use_column_width=True)

    with st.spinner("Analyzing image..."):

        image_bytes = uploaded_file.read()
        result = query_image(image_bytes)

        if isinstance(result, list) and "generated_text" in result[0]:
            caption = result[0]["generated_text"]
            st.success("Image Description:")
            st.write(caption)
        else:
            st.error("Model loading or API error. Try again.")
            st.write(result)
